#include <stdio.h>  

/*## 문제 5: 포인터를 이용한 연산

**요구사항:**
정수형 변수 `x`를 선언하고 5를 할당하세요.
정수형 포인터 `p_x`가 `x`를 가리키게 하세요.
포인터 `p_x`를 사용하여 `x`의 값에 10을 더하세요.
`x`의 최종 값을 출력하세요.*/

int main() {
    int x = 5;          // 정수형 변수 x 선언 및 초기화
    int* p_x = &x;      // 포인터 p_x가 x를 가리키도록 설정
    *p_x += 10;         // 포인터를 사용하여 x의 값에 10을 더함
    printf("연산 후 x의 값: %d\n", x); // x의 최종 값을 출력

    return 0;
}